#!/usr/bin/env python3

import os
import subprocess

env_file = '/etc/.env'
if os.path.exists(env_file):
    with open(env_file) as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith('#') and '=' in line:
                k, v = line.split('=', 1)
                os.environ[k.strip()] = v.strip()
subprocess.run(["apache2-foreground"])
